package ex_14072024;

public class Lab9 {
    public static void main(String[] args) {
        int a=10,b=10,c=10;

        System.out.println(a++);
        System.out.println(a);

        System.out.println(++b);
        System.out.println(b);

        System.out.println(c);

        System.out.println(a--);
        System.out.println(a);

        System.out.println(--b);
        System.out.println(b);
    }
}
